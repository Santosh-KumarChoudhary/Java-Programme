
public class PrimeCheck {
	
	
	public static void main(String[] args) {
		
		int number=3;
		
		
		
		if(number%2==0 && number>3 || number %3==0 && number>3)
			
		{
			System.out.println("Number "+ number + " is not a prime");
		}
		else
		{
			System.out.println(number +" is a prime");
		}
		
		
	}

}
